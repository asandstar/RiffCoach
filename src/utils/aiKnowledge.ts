/** 根据自评分数获取严重程度 */
export function getSeverityFromRating(rating: number): SeverityLevel {
  if (rating <= 2) return 'severe';
  if (rating <= 3) return 'moderate';
  return 'mild';
}

/** 使用日期作为种子，从数组中伪随机选择一项（同一天返回相同结果） */
export function pickByDate<T>(arr: T[], offset: number = 0): T {
  const today = new Date();
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate() + offset;
  return arr[seed % arr.length];
}

/** 从数组中随机选择一项（真随机） */
export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}
